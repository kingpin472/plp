num1=float(input("enter first number: "))
op=input("Enter operator: ")
num2=float(input("enter second number number: "))

if op == "+":
    print(num1 + num2)
elif op =="-" :
    print(num1 - num2)
elif op=="/":
    print(num1/num2)
elif op=="*":
    print(num1*num2)     
else:
    print("Invalid input")      


